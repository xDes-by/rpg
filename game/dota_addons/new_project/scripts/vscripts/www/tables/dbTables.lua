function GenerateHeroLocalize(PlayerHero)
    local hero_name = PlayerHero.hero_name
    local talents = PlayerHero.list
    local ThisHeroLocalize = AbilityLocalize[hero_name]
    local CompletedLocalization = ThisHeroLocalize.BasicAbilities
    for talent_number, talent_changes_array in pairs(ThisHeroLocalize.AbilityTalents) do
        local talent_level = talents[talent_number]
        if talent_level > 0 then
            for changes_ability_number, changes_types_array in pairs(talent_changes_array) do
                for additional_desc_ability_type, additional_desc_ability_type_value in pairs(changes_types_array) do

                    local desc = additional_desc_ability_type_value.desc
                    local params = additional_desc_ability_type_value.params
                    local cost = additional_desc_ability_type_value.cost

                    if CompletedLocalization[changes_ability_number][additional_desc_ability_type] == nil then
                        CompletedLocalization[changes_ability_number][additional_desc_ability_type] = {}
                    end
                    
                    if desc ~= nil then
                        if CompletedLocalization[changes_ability_number][additional_desc_ability_type].desc == nil then 
                            CompletedLocalization[changes_ability_number][additional_desc_ability_type].desc = {}
                        end
                        for desc_key, desc_text in pairs(desc) do
                            CompletedLocalization[changes_ability_number][additional_desc_ability_type].desc[desc_key] = desc_text
                        end
                    end

                    if params ~= nil then
                        if CompletedLocalization[changes_ability_number][additional_desc_ability_type].params == nil then 
                            CompletedLocalization[changes_ability_number][additional_desc_ability_type].params = {}
                        end
                        for params_key, params_date in pairs(params) do
                            if type(params_date.value[1]) == "table" then
                                params_date.value = params_date.value[talent_level]
                            end
                            CompletedLocalization[changes_ability_number][additional_desc_ability_type].params[params_key] = params_date
                        end
                    end

                    if cost ~= nil then
                        if CompletedLocalization[changes_ability_number][additional_desc_ability_type].cost == nil then 
                            CompletedLocalization[changes_ability_number][additional_desc_ability_type].cost = {}
                        end
                        for cost_key, cost_date in pairs(cost) do
                            if type(cost_date[1]) == "table" then
                                cost_date = cost_date[talent_level]
                            end
                            CompletedLocalization[changes_ability_number][additional_desc_ability_type].cost[cost_key] = cost_date
                        end
                    end

                end
            end
        end
    end
    return CompletedLocalization
end

